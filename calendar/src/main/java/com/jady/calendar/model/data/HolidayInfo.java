package com.jady.calendar.model.data;

public class HolidayInfo {
    private int year;
    private String[][] day;

    public int getYear() {
        return this.year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public String[][] getDay() {
        return this.day;
    }

    public void setDay(String[][] day) {
        this.day = day;
    }
}
